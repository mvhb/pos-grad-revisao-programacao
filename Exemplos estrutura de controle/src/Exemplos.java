
public class Exemplos {

	public static void main(String[] args) {
		for (int cont= 0; cont < 10; cont++) {
			System.out.println(cont);
		}
		
		int cont2 = 0;
		while (cont2 < 10) {
			System.out.println(cont2);
			cont2++;
		}
		
		int cont3 = 0;
		do {
			System.out.println(cont3);
			cont3++;
		} while (cont3 < 10);
	}

}
